#!/usr/bin/env python3
"""concat module"""


def concat(str1: str, str2: str) -> str:
    """
    Returns the concatenation of str1 and str2.

    Parameters
    ----------
    str1 : str
        first string
    str2 : str
        second string

    Returns
    -------
    str
        the concatenation of str1 and str2
    """
    return str1 + str2
