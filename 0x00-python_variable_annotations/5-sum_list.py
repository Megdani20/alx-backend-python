#!/usr/bin/env python3
"""5-sum_list module"""
import typing


def sum_list(input_list: typing.List[float]) -> float:
    """
    Returns the sum of a list of floats.

    Parameters
    ----------
    input_list : list[float]
        list of floats

    Returns
    -------
    float
        the sum of the list
    """
    return sum(input_list)
